package visualization.tool;

public class Runnable {
	
	private String name;
	private float time;
	
	public void set_name(String name) {
		this.name = name;
	}
	
	public String get_name() {
		return name;
	}
	
	public float get_time() {
		return time;
	}
	
	public void set_time(float time) {
		this.time = time;
	}
	
}
